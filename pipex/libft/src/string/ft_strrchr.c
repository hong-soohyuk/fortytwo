#include "../../includes/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = ft_strlen(s);
	if ((char)c == '\0')
		return ((char *)&s[i]);
	while (--i > -1)
		if (s[i] == (char)c)
			return ((char *)&s[i]);
	return (0);
}
